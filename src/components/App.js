import React, { Component } from 'react';
import './App.css';
import Square from './Square';

export default class App extends Component {
  constructor(props) {
    super(props);

    const squares = [];
    const numOfSquares = 24;
    for (let i = 0; i < numOfSquares; i++) {
      squares.push({ id: i, color: this.randomSquareColor() });
    }

    this.state = { squares };
  }

  randomSquareColor() {
    let randomNum = Math.floor(Math.random() * this.props.listAllColors.length);
    return this.props.listAllColors[randomNum];
  }

  render() {
    const squares = this.state.squares.map((square) => {
      return <Square key={square.id} color={square.color} />;
    });

    return (
      <div className="App">
        <h1 className="main-heading">Enjoy Disco Colors!</h1>
        <div className="squares">{squares}</div>
      </div>
    );
  }
}

